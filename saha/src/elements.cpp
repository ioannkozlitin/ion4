TElement::TElement(unsigned int z, double rCoeff, bool correctV0)
{
    if(z < 1) z = 1;
    if(z > 103) z = 103;
    Z = z;A = saha::GetA(z);
    //ro = elem_Ro[z-1];
    ro = saha::GetRo(z);

    fi.resize(z);
    cumFi.resize(z);
    v.resize(z+1);

    logG.resize(z+1, log(2.0));
    logG[z] = 0;

    //printf("z = %d : ",z);

    double sum = 0;
    for(unsigned int i = 0; i < z; i++)
    {
        //fi[i] = elem_fi[z-1][i] / eFi;
#ifdef NIST_IONIZATION_POTENTIALS
        if(z <= 100) fi[i] = nistIon[z-1][i] / eFi;
        else fi[i] = elem_fi[z-1][i] / eFi;
#else
        fi[i] = elem_fi[z-1][i] / eFi;
#endif

        //printf("%g ",nistIon[z-1][i] / elem_fi[z-1][i]);

        //double A = 0.53, B = 0.0347;
        //fi[i] = A * (i+1) * z / (z - i) + B * (i+1) * i;
        //printf("%g ",fi[i]);
        v[i] = 4/3.0 * M_PI * pow(rCoeff * (i+1) / fi[i], 3.0);
        sum += fi[i];cumFi[i] = sum;
    }
    //printf("\n");

    if(correctV0 && (ro > 0))
    {
        double v0 = A * eRo / ro;
        //printf("R = %g\n",v0 / v[0]);
        v[0] = v0;
    }

    v[z] = 0;
}

